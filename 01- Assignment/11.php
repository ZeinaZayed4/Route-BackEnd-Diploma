<?php

$x = 4;

switch ($x) {
	case 1:
		echo "It's Saturday.";
		break;
	case 2:
		echo "It's Sunday.";
		break;
	case 3:
		echo "It's Monday.";
		break;
	case 4:
		echo "It's Tuesday.";
		break;
	case 5:
		echo "It's Wednesday.";
		break;
	case 6:
		echo "It's Thursday.";
		break;
	case 7:
		echo "It's Friday.";
		break;
	default:
		echo "Enter a number between 1 and 7.";
}
